package com.example.simple_service_webapp;

public class City {
	String cityName;
	String description;
	String relatedLinks;
	String imgUrl;
	String idAdmins;
	public String getCityName() {
		return cityName;
	}
	public void setCityName(String cityName) {
		this.cityName = cityName;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getRelatedLinks() {
		return relatedLinks;
	}
	public void setRelatedLinks(String relatedLinks) {
		this.relatedLinks = relatedLinks;
	}
	public String getImgUrl() {
		return imgUrl;
	}
	public void setImgUrl(String imgUrl) {
		this.imgUrl = imgUrl;
	}
	public String getIdAdmins() {
		return idAdmins;
	}
	public void setIdAdmins(String idAdmins) {
		this.idAdmins = idAdmins;
	}




}
